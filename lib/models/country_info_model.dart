  String name;
  String capital;
  int population;
  String region;
  String subregion;
  String flag;
  String official;

  String continent;

  CountryInfo(
      {required this.name,
      required this.capital,
      required this.population,
      required this.region,
      required this.subregion,
      required this.flag,
      required this.continent,
      required this.official});
}
